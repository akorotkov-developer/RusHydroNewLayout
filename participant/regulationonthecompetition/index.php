	<li>обоснование актуальности проблемы и причины ее возникновения (не более 2-3 стр.); </li>
	<li>практические варианты и подходы к решению проблемы (в том числе технические и финансовые аспекты). </li>
</ul>
<ul>
	<li>2.8.3. Требования к оформлению: </li>
</ul>
<ul>
