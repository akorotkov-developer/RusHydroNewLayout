<p align="center">
	 2. КОНКУРСНЫЕ РАБОТЫ
</p>
<p align="justify">
	 2.1. Конкурс проводится для студентов и аспирантов российских технических вузов.
<br><br>
	 2.2. Конкурсные работы могут быть подготовлены как индивидуально, так и авторским коллективом.
<br><br>
	 2.3. Для участия в Конкурсе на сайте Конкурса заполняется заявка, к которой прикрепляется файлы с конкурсной работой, приложениями к ней, а также согласие на обработку персональных данных.
<br><br>
	 2.4. К участию в Конкурсе допускаются проекты, исследовательские и аналитические работы, в том числе курсовые и дипломные работы.
<br><br>
	 2.5. Конкурс проводится по 2 направлениям (номинациям): конкурс учебных работ и конкурс исследовательских проектов.
<br><br>
	 2.6. Темы учебных работ:
</p>
<ul>
	<li>2.6.1. Научно-техническое обоснование ГЭС: современные методы принятия решения о выборе створов и параметров проектируемых ГЭС, новые методы инженерных расчетов, моделирование, риски и экономика. Современные методы оценки воздействия гидроэнергетических объектов на окружающую среду. </li>
	<li>2.6.2. Современные подходы к мониторингу и диагностике состояния оборудования и гидротехнических сооружений ГЭС и управлению состоянием оборудования и гидротехнических сооружений. </li>
	<li>2.6.3. Современные методы управления водноэнергетическими режимами каскадов ГЭС. </li>
	<li>2.6.4. Гидроэнергетика и экология. </li>
	<li>2.6.5. Теплоэнергетика и экология. </li>
	<li>2.6.6. Энергетические комплексы: новые компоновки, технические и технологические решения. </li>
	<li>2.6.7. Современные технологии строительства ГЭС/ГАЭС, ТЭС, сетевых комплексов. </li>
	<li>2.6.8. Перспективные технологии аккумулирования и хранения энергии. </li>
	<li>2.6.9. Безопасность и надежность энергообъектов. </li>
	<li>2.6.10. Энергоэффективные решения в гидро- и теплоэнергетике. </li>
	<li>2.6.11. Возобновляемые источники энергии. Стратегия развития альтернативной энергетики. </li>
	<li>2.6.12. Виды, технологии формирования и свойства вторичных продуктов сжигания угольного топлива (золошлаковых материалов) и их роль в обеспечении экологической безопасности и ресурсной эффективности угольных ТЭС и котельных. </li>
</ul>
<p>
	 2.7. Темы исследовательских проектов:
</p>
<ul>
	<li>2.7.1. Разработка типового проекта мини-ГЭС контейнерного типа в действующих створах гидротехнических объектов. </li>
	<li>2.7.2. Автоматизация расчета режима работы каскада ГЭС на основе требований водопользователей. </li>
	<li>2.7.3. Методы интеллектуального прогнозирования отказов оборудования электроэнергетических предприятий. </li>
	<li>2.7.4. Применение систем видеоаналитики для повышения безопасности и надежности энергообъектов. </li>
	<li>2.7.5. Внедрение на ТЭС системы непрерывного контроля и учета вредных выбросов ТЭС в атмосферу в соответствии с Федеральным законом «Об охране окружающей среды». </li>
</ul>
<p>
	 2.8. Требования к конкурсной работе:
</p>
<ul>
	<li>2.8.1 Структура: </li>
</ul>
<ul>
	<li>титульный лист (указывается название Конкурса, номинация, тема работы / проекта, Ф.И.О. автора (авторов), наименование учебного заведения, факультета, кафедры, специальности, а также курс обучения и данные о научном руководителе – Ф.И.О., ученая степень, звание); </li>
	<li>содержание; </li>
	<li>приложения; </li>
	<li>список использованной литературы. </li>
</ul>
<p align="justify">
