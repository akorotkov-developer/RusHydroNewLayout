<p align="center">
	 4. РЕГЛАМЕНТ КОНКУРСА
</p>
<p align="justify">
	 4.1. Информационная поддержка Конкурса.
<br><br>
Ход проведения Конкурса, в том числе список финалистов, победителей и лауреатов Конкурса, освещается на сайте Конкурса и официальном сайте Общества <a href="http://www.rushydro.ru/">www.rushydro.ru</a>.
<br><br>
	 4.2. Сроки проведения Конкурса:
</p>
<ul>
	<li>4.2.1. Прием конкурсных работ – до 05.02.2020. </li>
	<li>4.2.2. Проведение дистанционного этапа Конкурса (оценка конкурсных работ, определение финалистов этапа) – до 20.03.2020. </li>
	<li>4.2.3. Проведение очного этапа Конкурса и награждение победителей и лауреатов Конкурса – апрель 2020 года. </li>
</ul>
<p align="justify">
4.3. Оргкомитет размещает на сайте Конкурса список полученных конкурсных работ. Список ежедневно обновляется.
<br><br>
При отсутствии конкурсной работы в списке на сайте Конкурса участник должен незамедлительно обратиться в Оргкомитет.
<br><br>
4.4. Ассоциация граждан и организаций по содействию развитию корпоративного образования «МАКО» (далее – Ассоциация) проводит предварительную оценку представленных конкурсных работ в системе обнаружения текстовых заимствований в программе «Антиплагиат».
<br><br>
Допускается не более 25 % заимствований.
<br><br>
Конкурсные работы, имеющие процент заимствований выше, не допускаются для проведения оценки Экспертным советом.
<br><br>
4.5. Экспертный совет проводит оценку конкурсных работ на основании следующих критериев:
</p>
<ul>
	<li>постановка задачи (масштаб и актуальность темы); </li>
	<li>степень личного участия в работе / проекте; </li>
	<li>полнота раскрытия темы и обоснованность выводов; </li>
	<li>новизна результатов (оригинальность решения); </li>
	<li>научный стиль изложения, аналитичность; </li>
	<li>качество оформления работы / проекта. </li>
</ul>
<p align="justify">
4.6. На основании оценки Экспертного совета Жюри определяет финалистов дистанционного этапа Конкурса.
<br><br>
4.7. Финалисты дистанционного этапа Конкурса по указанным ими в заявках электронным адресам и телефонам приглашаются Оргкомитетом для участия в очном этапе Конкурса.
<br><br>
Конкретное место и дата проведения очного этапа Конкурса определяются Оргкомитетом дополнительно и подлежат своевременному опубликованию на сайте Конкурса.
<br><br>
4.8. В ходе проведения очного этапа Конкурса участники выполняют конкурсное задание: подготовка презентации по теме, объявленной непосредственно перед началом этапа.
<br><br>
По итогам презентаций Жюри определяет победителей Конкурса на основании следующих критериев:
</p>
<ul>
	<li>гибкость и системность мышления; </li>
	<li>лидерство; </li>
	<li>планирование и организация; </li>
	<li>коммуникативная компетенция; </li>
	<li>ответственность и результативность; </li>
	<li>самостоятельность; </li>
	<li>профессионализм. </li>
</ul>
<p align="justify">
4.9. Оргкомитет может учредить специальные призовые номинации Конкурса.
<br><br>
4.10. Организацию награждения победителей и лауреатов Конкурса осуществляет Ассоциация.
